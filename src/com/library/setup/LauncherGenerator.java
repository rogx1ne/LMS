package com.library.setup;

import java.io.*;
import java.nio.file.Files;
import java.nio.file.attribute.PosixFilePermission;
import java.util.HashSet;
import java.util.Set;

/**
 * Generates platform-specific launcher scripts for LMS
 */
public class LauncherGenerator {
    
    private File installDirectory;
    private SystemEnvironment.OSType osType;
    
    public LauncherGenerator(File installDirectory, SystemEnvironment.OSType osType) {
        this.installDirectory = installDirectory;
        this.osType = osType;
    }
    
    /**
     * Compile all Java sources once during installation
     */
    public void compileAllSources() throws Exception {
        System.out.println("Compiling all Java sources...");
        
        File srcDir = new File(installDirectory, "src");
        File binDir = new File(installDirectory, "bin");
        File libDir = new File(installDirectory, "lib");
        
        if (!binDir.exists()) {
            binDir.mkdirs();
        }
        
        // Build classpath
        StringBuilder classpath = new StringBuilder();
        File[] libFiles = libDir.listFiles((dir, name) -> name.endsWith(".jar"));
        if (libFiles != null) {
            for (File jar : libFiles) {
                if (classpath.length() > 0) {
                    classpath.append(osType == SystemEnvironment.OSType.WINDOWS ? ";" : ":");
                }
                classpath.append(jar.getAbsolutePath());
            }
        }
        
        // Find all Java files
        StringBuilder javaFiles = new StringBuilder();
        findJavaFiles(srcDir, javaFiles);
        
        // Compile command
        String javacCmd;
        if (osType == SystemEnvironment.OSType.WINDOWS) {
            javacCmd = "javac -d \"" + binDir.getAbsolutePath() + "\" -cp \"" + classpath + "\" " + javaFiles;
        } else {
            javacCmd = "javac -d " + binDir.getAbsolutePath() + " -cp \"" + classpath + "\" " + javaFiles;
        }
        
        Process process = Runtime.getRuntime().exec(javacCmd);
        
        // Read output
        BufferedReader stdOut = new BufferedReader(new InputStreamReader(process.getInputStream()));
        BufferedReader stdErr = new BufferedReader(new InputStreamReader(process.getErrorStream()));
        
        String line;
        while ((line = stdOut.readLine()) != null) {
            System.out.println(line);
        }
        while ((line = stdErr.readLine()) != null) {
            System.err.println(line);
        }
        
        int exitCode = process.waitFor();
        if (exitCode != 0) {
            throw new Exception("Compilation failed with exit code: " + exitCode);
        }
        
        System.out.println("Compilation successful!");
    }
    
    private void findJavaFiles(File dir, StringBuilder result) {
        File[] files = dir.listFiles();
        if (files != null) {
            for (File file : files) {
                if (file.isDirectory()) {
                    findJavaFiles(file, result);
                } else if (file.getName().endsWith(".java")) {
                    if (result.length() > 0) {
                        result.append(" ");
                    }
                    result.append("\"").append(file.getAbsolutePath()).append("\"");
                }
            }
        }
    }
    
    /**
     * Generate launcher script for the installed platform
     */
    public File generateLauncher() throws IOException {
        if (osType == SystemEnvironment.OSType.WINDOWS) {
            return generateWindowsLauncher();
        } else {
            return generateLinuxLauncher();
        }
    }
    
    /**
     * Generate Windows batch file launcher
     */
    private File generateWindowsLauncher() throws IOException {
        File launcherFile = new File(installDirectory, "LMS-Launcher.bat");
        
        StringBuilder script = new StringBuilder();
        script.append("@echo off\n");
        script.append("REM Library Management System Launcher\n");
        script.append("REM Auto-generated by LMS Setup Wizard\n\n");
        
        script.append("cd /d \"%~dp0\"\n\n");
        
        script.append("REM Check if Java is available\n");
        script.append("java -version >nul 2>&1\n");
        script.append("if %errorlevel% neq 0 (\n");
        script.append("    echo Error: Java is not installed or not in PATH\n");
        script.append("    echo Please install Java 8 and try again\n");
        script.append("    pause\n");
        script.append("    exit /b 1\n");
        script.append(")\n\n");
        
        script.append("REM Launch LMS Application\n");
        script.append("echo Starting Library Management System...\n");
        script.append("java -cp \"bin;lib\\*\" com.library.ui.DashboardFrame\n\n");
        
        script.append("if %errorlevel% neq 0 (\n");
        script.append("    echo.\n");
        script.append("    echo Application exited with error code: %errorlevel%\n");
        script.append("    pause\n");
        script.append(")\n");
        
        writeFile(launcherFile, script.toString());
        
        return launcherFile;
    }
    
    /**
     * Generate Linux shell script launcher
     */
    private File generateLinuxLauncher() throws IOException {
        File launcherFile = new File(installDirectory, "LMS-Launcher.sh");
        
        StringBuilder script = new StringBuilder();
        script.append("#!/bin/bash\n");
        script.append("# Library Management System Launcher\n");
        script.append("# Auto-generated by LMS Setup Wizard\n\n");
        
        script.append("# Get the directory where this script is located\n");
        script.append("SCRIPT_DIR=\"$( cd \"$( dirname \"${BASH_SOURCE[0]}\" )\" && pwd )\"\n");
        script.append("cd \"$SCRIPT_DIR\"\n\n");
        
        script.append("# Check if Java is available\n");
        script.append("if ! command -v java &> /dev/null; then\n");
        script.append("    echo \"Error: Java is not installed or not in PATH\"\n");
        script.append("    echo \"Please install Java 8 and try again\"\n");
        script.append("    read -p \"Press Enter to exit...\"\n");
        script.append("    exit 1\n");
        script.append("fi\n\n");
        
        script.append("# Launch LMS Application\n");
        script.append("echo \"Starting Library Management System...\"\n");
        script.append("java -cp \"bin:lib/*\" com.library.ui.DashboardFrame\n\n");
        
        script.append("EXIT_CODE=$?\n");
        script.append("if [ $EXIT_CODE -ne 0 ]; then\n");
        script.append("    echo \"\"\n");
        script.append("    echo \"Application exited with error code: $EXIT_CODE\"\n");
        script.append("    read -p \"Press Enter to exit...\"\n");
        script.append("fi\n");
        
        writeFile(launcherFile, script.toString());
        
        // Make executable on Linux
        makeExecutable(launcherFile);
        
        return launcherFile;
    }
    
    /**
     * Make file executable (Linux only)
     */
    private void makeExecutable(File file) {
        try {
            Set<PosixFilePermission> perms = new HashSet<>();
            perms.add(PosixFilePermission.OWNER_READ);
            perms.add(PosixFilePermission.OWNER_WRITE);
            perms.add(PosixFilePermission.OWNER_EXECUTE);
            perms.add(PosixFilePermission.GROUP_READ);
            perms.add(PosixFilePermission.GROUP_EXECUTE);
            perms.add(PosixFilePermission.OTHERS_READ);
            perms.add(PosixFilePermission.OTHERS_EXECUTE);
            Files.setPosixFilePermissions(file.toPath(), perms);
            System.out.println("Made executable: " + file.getName());
        } catch (IOException | UnsupportedOperationException e) {
            // Windows doesn't support POSIX permissions
            System.err.println("Could not set executable permission: " + e.getMessage());
        }
    }
    
    private void writeFile(File file, String content) throws IOException {
        try (FileWriter writer = new FileWriter(file)) {
            writer.write(content);
        }
        System.out.println("Generated launcher: " + file.getAbsolutePath());
    }
}
